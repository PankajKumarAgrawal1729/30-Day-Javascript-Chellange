const book = {
  title: "The Great Gatsby",
  author: "F. Scott Fitzgerald",
  year: 1925,
};

const keys = Object.keys(book);
console.log("Keys of Book: " + keys);
// Keys of Book: title,author,year

const values = Object.values(book);
console.log("Values of Book: " + values);
// Values of Book: The Great Gatsby,F. Scott Fitzgerald,1925
